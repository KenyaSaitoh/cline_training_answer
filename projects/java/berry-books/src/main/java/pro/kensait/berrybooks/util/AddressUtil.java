    public static boolean startsWithValidPrefecture(String address) {
        if (address == null || address.isBlank()) {
            return false;
        }
        
        for (String prefecture : PREFECTURES) {
            if (address.startsWith(prefecture)) {
                return true;
            }
        }
        return false;
    }
    
